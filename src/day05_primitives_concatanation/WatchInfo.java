package day05_primitives_concatanation;

public class WatchInfo {
    public static void main(String[] args) {
        String brand = "Apple";
        String color = "blue";
        double price = 345.00d;
        boolean waterResistant = true;
        boolean isSmart = true;
        char gender = 'F';
        System.out.println("Brand:\t\t\t\t\t"+brand);
        System.out.println("Color:\t\t\t\t\t"+color);
        System.out.println("Price:\t\t\t\t  $"+price);
        System.out.println("Water Resistant:\t "+waterResistant);
        System.out.println("Is Smart:\t\t\t\t "+isSmart);
        System.out.println("Gender:\t\t\t\t\t "+gender);


    }
}
